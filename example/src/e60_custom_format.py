"""Example showing how to extend mformat with a custom format.

This example demonstrates how to create a custom format class that extends
the mformat framework. The custom format is a simple "tree" format that uses
indentation and markers to show document structure. This format is easy to
understand but is clearly not a candidate for inclusion as a standard format
in the framework.
"""

